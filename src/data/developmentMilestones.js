export function getSafeFeedback(score) {
  if (score >= 80) {
    return 'Your child is showing many expected skills for this age range. Keep supporting development through daily play, reading, and positive interaction.';
  }

  if (score >= 50) {
    return 'Your child is showing some expected skills for this age range. A few areas may need more practice and support. Try the suggested activities and continue observing progress.';
  }

  return 'Your child may need more support in this area. Try the suggested activities and consult a child development specialist if concerned.';
}